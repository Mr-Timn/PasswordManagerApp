#ifndef PARAMS_H1024
#define PARAMS_H1024

#define KYBER1024_K 4
#define KYBER1024_NAMESPACE(s) pqcrystals_kyber1024_ref##s

#define KYBER1024_N 256
#define KYBER1024_Q 3329

#define KYBER1024_SYMBYTES 32   /* size in bytes of hashes, and seeds */
#define KYBER1024_SSBYTES  32   /* size in bytes of shared key */

#define KYBER1024_POLYBYTES		384
#define KYBER1024_POLYVECBYTES	(KYBER1024_K * KYBER1024_POLYBYTES)

#define KYBER1024_ETA1 2
#define KYBER1024_POLYCOMPRESSEDBYTES    160
#define KYBER1024_POLYVECCOMPRESSEDBYTES (KYBER1024_K * 352)

#define KYBER1024_ETA2 2

#define KYBER1024_INDCPA_MSGBYTES       KYBER1024_SYMBYTES
#define KYBER1024_INDCPA_PUBLICKEYBYTES (KYBER1024_POLYVECBYTES + KYBER1024_SYMBYTES)
#define KYBER1024_INDCPA_SECRETKEYBYTES (KYBER1024_POLYVECBYTES)
#define KYBER1024_INDCPA_BYTES          (KYBER1024_POLYVECCOMPRESSEDBYTES \
                                     + KYBER1024_POLYCOMPRESSEDBYTES)

#define KYBER1024_PUBLICKEYBYTES  (KYBER1024_INDCPA_PUBLICKEYBYTES)
/* 32 bytes of additional space to save H(pk) */
#define KYBER1024_SECRETKEYBYTES  (KYBER1024_INDCPA_SECRETKEYBYTES \
                               + KYBER1024_INDCPA_PUBLICKEYBYTES \
                               + 2*KYBER1024_SYMBYTES)
#define KYBER1024_CIPHERTEXTBYTES  KYBER1024_INDCPA_BYTES

#endif
